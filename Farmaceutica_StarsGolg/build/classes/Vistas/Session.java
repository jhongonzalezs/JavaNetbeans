    private void SessionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SessionesActionPerformed

        if (User.getText().equals("") && Pass.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escribir El Usuario y La Contrase単a");
        } else {
            if (User.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Escribir El Usuario");
            } else {
                if (Pass.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Escribir La Contase単a");
                } else {
                    if (User.getText().equals("1004131238") && Pass.getText().equals("12345")) {
                        Inicio In = new Inicio();
                        In.setLocationRelativeTo(null);
                        In.setVisible(true);
                        this.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(null, "Usuario/Contrase単a Incorrecta");
                    }
                }
            }
        }
    }//GEN-LAST:event_SessionesActionPerformed

