        int n,a, fibo,b;
        b = 1;
        a=1;
        
        n = Integer.parseInt(txtNumero.getText());
        if (n == 1){
           tafibonacci.setText("1" + "\n"); 
        }else{
        tafibonacci.setText("1" + "\n");
        tafibonacci.append("1" + "\n");
        
        for(int i=3;i<=n;i++){
            fibo = a + b;
            tafibonacci.append("" + fibo + "\n");
            a = b;
            b = fibo;
        }  
        }
        


