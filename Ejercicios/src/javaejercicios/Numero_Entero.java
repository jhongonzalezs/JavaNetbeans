    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        // TODO add your handling code here:
        
        int Num;
        
        
        if (txtNum.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Digite el numero");
            txtNum.transferFocus();
        }
        
        else {
            Num=Integer.parseInt(txtNum.getText());
        
            if (Num>0){
                JOptionPane.showMessageDialog(null, "El Numero.." +Num+ ".. Es Positivo");
                txtValidar.setText(Integer.toString(Num));
                }
                if (Num<0){
                    JOptionPane.showMessageDialog(null, "El Numero.." +Num+ ".. Es Negativo");
                    txtValidar.setText(Integer.toString(Num));
                    }
                    
                    if (Num==0){
                        JOptionPane.showMessageDialog(null, "El Numero."+Num+"..Es Nulo");
                        txtValidar.setText(Integer.toString(Num));
                        
                        txtValidar.setText(Integer.toString(Num));
                        
                    }  
            
        }
        
    
    }//GEN-LAST:event_btnValidarActionPerformed

