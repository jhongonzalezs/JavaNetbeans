    private void IniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IniciarActionPerformed
        if (Correo.getText().equals("") && Pass.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Los Campos Son Obligatorios");
        } else {
            if (Correo.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "El Campo Correo Es Obligatorio");
            } else {
                if (Pass.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "El Campo Contraseña Es Obligatorio");
                }
            }
        }
        Connection con = null;
        try {
            con = getConnection();
            ps = con.prepareStatement("SELECT * FROM registros WHERE correo = ? AND contrasena = ?");
            ps.setString(1, Correo.getText());
            ps.setString(2, Pass.getText());

            rs = ps.executeQuery();

            if (rs.next()) {
                Inicio sig = new Inicio();
                sig.setLocationRelativeTo(null);
                sig.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Lo Sentimos Ese Usuario No Existe");
            }

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_IniciarActionPerformed


    private void registrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrosActionPerformed
        Connection con = null;
        try {

            con = getConnection();
            ps = con.prepareStatement("INSERT INTO registros (nombre,cedula,direccion,correo,contrasena,celular) VALUES (?,?,?,?,?,?)");
            ps.setString(1, nombre.getText());
            ps.setString(2, cedula.getText());
            ps.setString(3, direcion.getText());
            ps.setString(4, electro.getText());
            ps.setString(5, contrasena.getText());
            ps.setString(6, telefono.getText());

            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Usuario Registrado Correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Error Al Registrar ");
            }

            con.close();

        } catch (Exception e) {
            System.out.println(e);
        }

        limpiar();
    }//GEN-LAST:event_registrosActionPerformed

