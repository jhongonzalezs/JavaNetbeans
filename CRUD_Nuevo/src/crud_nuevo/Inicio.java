    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        Connection con = null;
        
        try {
            con = getConnection();
            ps = con.prepareStatement("SELECT * FROM registros WHERE cedula = ?");
            ps.setString(1, txtcedula.getText());
            
            rs = ps.executeQuery();
            
            if(rs.next()){
                txtId.setText(rs.getString("id_user"));
                txtnombre.setText(rs.getString("nombre"));
                txtDireccion.setText(rs.getString("direccion"));
                txtTelefono.setText(rs.getString("celular"));
                txtCorreo.setText(rs.getString("correo"));
                txtpass.setText(rs.getString("contrasena"));
                
            }else{
                JOptionPane.showMessageDialog(null, "No Existe Un Usuario Con Ese Numero De Identificacion ");
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_BuscarActionPerformed

