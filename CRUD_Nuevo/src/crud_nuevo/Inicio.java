    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        Connection con = null;
        
        try {
            con = getConnection();
            ps = con.prepareStatement("UPDATE registros SET nombre=?, direccion=?, celular=?, correo=?, contrasena=?");
            ps.setString(1, txtnombre.getText());
            ps.setString(2, txtDireccion.getText());
            ps.setString(3, txtcedula.getText());
            ps.setString(4, txtCorreo.getText());
            ps.setString(5, txtpass.getText());
            
            int res = ps.executeUpdate();
            
            if(res > 0){
                JOptionPane.showMessageDialog(null, "Usuario Actualizado Correctamente");
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "El Usuario No Pudo Ser Actualizado");
                limpiar();
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_actualizarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        Connection con = null;
        
        try {
            con = getConnection();
            ps = con.prepareStatement("DELETE FROM registros WHERE cedula = ?");
            ps.setInt(1, Integer.parseInt(txtcedula.getText()));
            
            int res = ps.executeUpdate();
            
            if(res > 0){
                JOptionPane.showMessageDialog(null, "Usuario Eliminado Correctamente");
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "El Usuario No Pudo Ser Eliminado");
                limpiar();
            }
            
            con.close();
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_eliminarActionPerformed

